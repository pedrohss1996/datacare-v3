-- 3. QUERY TEMPORAL - PARA ANÁLISE DE TENDÊNCIA
SELECT 
    TO_CHAR(ADD_MONTHS(TRUNC(SYSDATE, 'MM'), -LEVEL+1), 'MM/YYYY') AS PERIODO,
    ROUND(DBMS_RANDOM.VALUE(100, 500)) AS ATENDIMENTOS,
    ROUND(DBMS_RANDOM.VALUE(50, 150), 2) AS RECEITA
FROM DUAL
CONNECT BY LEVEL <= 12
ORDER BY 1;

-- 4. QUERY COM PERCENTUAIS - PARA GRÁFICO DE PIZZA
