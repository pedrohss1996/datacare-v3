UNION ALL
SELECT 'Unimed', 40, 2000 FROM DUAL
UNION ALL
SELECT 'SUS', 15, 800 FROM DUAL
UNION ALL
SELECT 'Outros', 10, 500 FROM DUAL;

-- 5. QUERY COMPLETA - DASHBOARD EXECUTIVO
SELECT 
    TO_CHAR(TRUNC(SYSDATE) - LEVEL + 1, 'DD/MM') AS DIA,
    ROUND(DBMS_RANDOM.VALUE(50, 150)) AS CONSULTAS,
    ROUND(DBMS_RANDOM.VALUE(10, 30)) AS INTERNACOES,
    ROUND(DBMS_RANDOM.VALUE(5000, 15000), 2) AS FATURAMENTO,
    ROUND(DBMS_RANDOM.VALUE(70, 95), 1) AS TAXA_OCUPACAO
FROM DUAL
CONNECT BY LEVEL <= 30
ORDER BY 1;

